package com.unconv.spring.enums;

/** Enumeration representing default user roles in the system. */
public enum DefaultUserRole {
    /** Represents an unconverted user role. */
    UNCONV_USER,

    /** Represents an unconverted admin role. */
    UNCONV_ADMIN,

    /** Represents an unconverted tenant role. */
    UNCONV_TENANT,

    /** Represents an unconverted manager role. */
    UNCONV_MANAGER,
}
